package com.joe.retailforest.item;

public class RequestTradeInfo {
	
	
	private String merchantID;
	
	private String respondType;
	
	private String timeStamp;
	
	private String version;
	
	private String merchantOrderNo;
	
	private String amt;
	
	private String itemDesc;
	
	private String returnURL;
	
	private String notifyURL;
	
	private String customerURL;
	
	private String clientBackURL;
	
	private String email;
	
	private String credit;

	private String indexType;

	public String getMerchantID() {
		return merchantID;
	}

	public void setMerchantID(String merchantID) {
		this.merchantID = merchantID;
	}

	public String getRespondType() {
		return respondType;
	}

	public void setRespondType(String respondType) {
		this.respondType = respondType;
	}

	public String getTimeStamp() {
		return timeStamp;
	}

	public void setTimeStamp(String timeStamp) {
		this.timeStamp = timeStamp;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getMerchantOrderNo() {
		return merchantOrderNo;
	}

	public void setMerchantOrderNo(String merchantOrderNo) {
		this.merchantOrderNo = merchantOrderNo;
	}

	public String getAmt() {
		return amt;
	}

	public void setAmt(String amt) {
		this.amt = amt;
	}

	public String getItemDesc() {
		return itemDesc;
	}

	public void setItemDesc(String itemDesc) {
		this.itemDesc = itemDesc;
	}

	public String getReturnURL() {
		return returnURL;
	}

	public void setReturnURL(String returnURL) {
		this.returnURL = returnURL;
	}

	public String getNotifyURL() {
		return notifyURL;
	}

	public void setNotifyURL(String notifyURL) {
		this.notifyURL = notifyURL;
	}

	public String getCustomerURL() {
		return customerURL;
	}

	public void setCustomerURL(String customerURL) {
		this.customerURL = customerURL;
	}

	public String getClientBackURL() {
		return clientBackURL;
	}

	public void setClientBackURL(String clientBackURL) {
		this.clientBackURL = clientBackURL;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getCredit() {
		return credit;
	}

	public void setCredit(String credit) {
		this.credit = credit;
	}

	public String getIndexType() {
		return indexType;
	}

	public void setIndexType(String indexType) {
		this.indexType = indexType;
	}
}
